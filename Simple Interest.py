def Answer(P, R, T):
    print("This program is for calculating Simple Interest")
    print("Input Principal")
    P = int(input())
    print("Input Rate")
    R = int(input())
    print("Input Time")
    T = int(input())
    num = P*R*T
    Ans = num/100
    print("Simple Interest =", + Ans)
Answer("P", "R", "T")
